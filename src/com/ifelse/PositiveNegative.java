package com.ifelse;

public class PositiveNegative {

	public static void main(String[] args) {
		int number = -22;
		if (number > 0) {
			System.out.println("Number is positive");
		} else if (number < 0) {
			System.out.println("Number is negative");
		} else {
			System.out.println("It's zero");
		}
	}

}
